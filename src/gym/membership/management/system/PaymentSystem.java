    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        File db = new File("database.txt");
        String uid = txtid.getText();
        Boolean idfound = false;  
        try{
            Scanner read = new Scanner(db);
            while(read.hasNextLine()){
                String nl = read.nextLine();
                String column[]=nl.split("\t\t");
                if(column[0].equals(uid) && column[6].equals("New")){
                    lblid.setText(column[0]);
                    lblname.setText(column[1]);
                    lblcon.setText(column[2]);
                    lbltype.setText(column[3]);
                    lblreg.setText(column[4]);
                    lblfee1.setText(column[5]);
                    lblstat.setText(column[6]);
                    lblbill.setText(column[7]);
                    String strreg= column[4].replaceAll("[^0-9]", "");
                    String strfee= column[5].replaceAll("[^0-9]", "");
                    int fee = Integer.parseInt(strfee);
                    int reg = Integer.parseInt(strreg);
                    int total = reg + fee;
                    String strtotal = Integer.toString(total);
                    lblfee.setText("RM"+strtotal);
                    idfound = true;             
                    btnpay.setEnabled(true);
                    btnrecord.setEnabled(true);
                    javax.swing.JOptionPane.showMessageDialog(this, "ID found","Information",JOptionPane.INFORMATION_MESSAGE);
                    break;
                    }
                else if(column[0].equals(uid) && column[6].equals("Active")){
                    lblid.setText(column[0]);
                    lblname.setText(column[1]);
                    lblcon.setText(column[2]);
                    lbltype.setText(column[3]);
                    lblreg.setText(column[4]);
                    lblfee1.setText(column[5]);
                    lblstat.setText(column[6]);
                    lblbill.setText(column[7]);
                    Date date1 = DateFormat.getDateInstance().parse(column[7]);
                    Date today = new Date();
                    if (date1.before(today)){
                        long diff = today.getTime() - date1.getTime();
                        Calendar c = Calendar.getInstance();
                        c.setTimeInMillis(diff);
                        int mMonth = c.get(Calendar.MONTH)+1;
                        String strfee= column[5].replaceAll("[^0-9]", "");
                        int fee = Integer.parseInt(strfee);
                        int total = fee*mMonth;
                        lblfee.setText("RM"+total);
                    }
                    else {
                        
                        lblfee.setText("RM0");
                    }
                        
                    idfound = true;
                    btnpay.setEnabled(true);
                    btnrecord.setEnabled(true);
                    javax.swing.JOptionPane.showMessageDialog(this, "ID found","Information",JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
                else{
                    idfound = false;
                }
                    
                
            }
            if(idfound == false){
                javax.swing.JOptionPane.showMessageDialog(this, "ID not found","Error",JOptionPane.ERROR_MESSAGE);
                    
                    lblname.setText("");
                    lblstat.setText("");
                    lblbill.setText("");
                    lblfee.setText("");
            }
            
            

        }catch(FileNotFoundException e){
            //handle this
        } catch (ParseException ex) {
            Logger.getLogger(PaymentSystem.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnsearchActionPerformed


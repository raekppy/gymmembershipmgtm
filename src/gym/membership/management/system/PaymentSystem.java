    private void btnpayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpayActionPerformed
        try {
            // TODO add your handling code here:
            String strdate = lblbill.getText();
            Date date1 = DateFormat.getDateInstance().parse(strdate);
            Calendar myCal = Calendar.getInstance();
            myCal.setTime(date1);    
            myCal.add(Calendar.MONTH, +1);    
            date1 = myCal.getTime();
            String stringDate = DateFormat.getDateInstance().format(date1);
            String search = lblid.getText() +"\t\t"+lblname.getText()+"\t\t"+lblcon.getText()+"\t\t"+lbltype.getText()+"\t\t"+lblreg.getText()+"\t\t"+lblfee1.getText()+"\t\t"+lblstat.getText()+"\t\t"+lblbill.getText();
            String replacement = lblid.getText() +"\t\t"+lblname.getText()+"\t\t"+lblcon.getText()+"\t\t"+lbltype.getText()+"\t\t"+lblreg.getText()+"\t\t"+lblfee1.getText()+"\t\t"+"Active"+"\t\t"+stringDate;
            int reply = JOptionPane.showConfirmDialog(this, "Are you sure you want to pay your fee?", "Confirm", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                try {
                    
                    BufferedReader file = new BufferedReader(new FileReader("database.txt"));
                    String line;String input = "";
                    while ((line = file.readLine()) != null) input += line + "\r\n";
                    file.close();
                    
                    
                    
                    
                    input = input.replace(search, replacement);
                    
                    
                    
                    
                    FileOutputStream fileOut = new FileOutputStream("database.txt");
                    fileOut.write(input.getBytes());
                    fileOut.close();
                    
                } catch (Exception e) {
                    
                }
                
                FileWriter writer;
        try {
            writer = new FileWriter("payment.txt", true);
        
            writer.write(lblid.getText() + " \t\t" + lblname.getText() + "\t\t" + lblbill.getText() + "\t\t" + lblfee.getText() + "\t\t" + Login.username);
        
            writer.write("\r\n");
            writer.close();
            } catch (IOException ex) {
            Logger.getLogger(InsertNewMember.class.getName()).log(Level.SEVERE, null, ex);
        }
                
                
                javax.swing.JOptionPane.showMessageDialog(this, "Successfully Updated!", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                new StaffMenu().setVisible(true);
            }
        } catch (ParseException ex) {
            Logger.getLogger(PaymentSystem.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnpayActionPerformed


    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        String search = lblid.getText() +"\t\t"+lblname.getText()+"\t\t"+lblcontact.getText()+"\t\t"+lblmtype.getText()+"\t\t"+lblreg.getText()+"\t\t"+lblfee.getText();
        String replacement = lblid.getText()+"\t\t"+lblname.getText()+"\t\t"+contacttxt.getText()+"\t\t"+mtype.getSelectedItem()+"\t\t"+reg.getText()+"\t\t"+fee.getText();
        int reply = JOptionPane.showConfirmDialog(this, "Are you sure you want to make changes on this record?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
        try {
        
        BufferedReader file = new BufferedReader(new FileReader("database.txt"));
        String line;String input = "";
        while ((line = file.readLine()) != null) input += line + "\r\n";
        file.close();

        
        
        
        input = input.replace(search, replacement); 
        
       

        
        FileOutputStream fileOut = new FileOutputStream("database.txt");
        fileOut.write(input.getBytes());
        fileOut.close();

    } catch (Exception e) {
        
    }
        javax.swing.JOptionPane.showMessageDialog(this, "Successfully Updated!", "Success", JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(false);
        new StaffMenu().setVisible(true);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void mtypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mtypeActionPerformed
        // TODO add your handling code here:
        String membertype; 
        membertype = (String)mtype.getSelectedItem();
        switch (membertype) {
            case "Deluxe":
                reg.setText("RM500");
                fee.setText("RM120");
                break;
            case "Non-Deluxe":
                reg.setText("RM300");
                fee.setText("RM100");
                break;
            case "Week-Day":
                reg.setText("RM180");
                fee.setText("RM75");
                break;
            default:
                break;
        }

    }//GEN-LAST:event_mtypeActionPerformed


    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        File db = new File("database.txt");
        String uid = idtxt.getText();
        Boolean idfound = false;  
        try{
            Scanner read = new Scanner(db);
            while(read.hasNextLine()){
                String nl = read.nextLine();
                String column[]=nl.split("\t\t");
                if(column[0].equals(uid)){
                    lblid.setText(column[0]);
                    lblname.setText(column[1]);
                    contacttxt.setText(column[2]);
                    lblcontact.setText(column[2]);
                    mtype.setSelectedItem(column[3]);
                    lblmtype.setText(column[3]);
                    reg.setText(column[4]);
                    lblreg.setText(column[4]);
                    fee.setText(column[5]);
                    lblfee.setText(column[5]);
                    idfound = true;
                    btnupdate.setEnabled(true);
                    mtype.setEnabled(true);
                    javax.swing.JOptionPane.showMessageDialog(this, "ID found","Information",JOptionPane.INFORMATION_MESSAGE);
                    break;
                    }
                else{
                    idfound = false;
                }
                    
                
            }
            if(idfound == false){
                javax.swing.JOptionPane.showMessageDialog(this, "ID not found","Error",JOptionPane.ERROR_MESSAGE);
                    btnupdate.setEnabled(false);
                    mtype.setEnabled(false);
                    lblname.setText("");
                    contacttxt.setText("");
                    mtype.setSelectedItem("");
                    reg.setText("");
                    fee.setText("");
            }
            
            

        }catch(FileNotFoundException e){
            //handle this
        } 
    }//GEN-LAST:event_btnsearchActionPerformed

